public class ExemploForArray {
    public static void main(String[] args) {

        String alunos[] = { "Michael", "Han Solo", "Garapa", "Patrão" };

        for (int i = 0; i <= alunos.length - 1; i++) {
            System.out.println("O aluno do ínidce " + i + " é " + alunos[i]);
        }
    }
}
