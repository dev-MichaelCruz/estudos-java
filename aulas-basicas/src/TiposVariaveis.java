public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
        
        String meuNome = "Michael Cruz";
        System.out.println(meuNome);
        
        char sexo = 'M';
        System.out.println(sexo);
        
        double salario = 2000;
        System.out.println(salario);
        
        float numeroQuebrado = 2.80f;
        System.out.println(numeroQuebrado);
        
        double numeroQuebradoMaisPreciso = 3.91564;
        System.out.println(numeroQuebradoMaisPreciso);
        
        short numerCurto = 1;
        System.out.println(numerCurto);
        
        int numeroNormal = 125;
        System.out.println(numeroNormal);
        

        //mudando valor de uma vari√°vel
        int numero = 5;
        numero = 10;

        System.out.println(numero);
        
    }
}
